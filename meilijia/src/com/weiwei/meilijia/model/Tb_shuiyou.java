package com.weiwei.meilijia.model;

public class Tb_shuiyou {
	private String timedd;
	private String shui;
	private String you;
	public Tb_shuiyou() {
		super();
	}
	public Tb_shuiyou(String timedd, String shui, String you) {
		super();
		this.timedd = timedd;
		this.shui = shui;
		this.you = you;
	}
	public String getTimedd() {
		return timedd;
	}
	public void setTimedd(String timedd) {
		this.timedd = timedd;
	}
	public String getShui() {
		return shui;
	}
	public void setShui(String shui) {
		this.shui = shui;
	}
	public String getYou() {
		return you;
	}
	public void setYou(String you) {
		this.you = you;
	}
	
}
